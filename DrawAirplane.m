function DrawAirplane(xNorth, yUp, zEast, e0,e1,e2,e3)

%get the airplane vertices, faces, and colors
[Vertices, Faces, Colors] = AirplaneGraphics;

%Convert from the body reference frame to the inertial frame
%first rotate, then translate
Vertices = BodyToInertialRotation(Vertices, e0, e1, e2, e3);
Vertices = BodyToInertialTranslation(Vertices, xNorth, yUp, zEast);

% transform vertices from North-Up-East to East-North-Up
% (for matlab rendering)
R_ENU = [...
    0, 0, 1;... %east
    1, 0, 0;... %north
    0, 1, 0;... %up
    ];
Vertices = (R_ENU*Vertices')';

%draw the airplane using the "patch" command
%To learn more about the patch command, type "help patch" in the command
% window
hold on
patch('Vertices', Vertices, 'Faces', Faces,...
    'FaceVertexCData',Colors,...
    'FaceColor','flat');
title('Spacecraft')
xlabel('East (z)')
ylabel('North (x)')
zlabel('Up (y)')
view(32,47)  % set the view angle for the figure
axis([zEast-10,zEast+10,xNorth-10,xNorth+10,yUp-10,yUp+10]); %axis limits
pbaspect([1 1 1]) %Set the aspect ratio
grid on
hold off

end


